DROP VIEW TOVAR_VW;

/*-------------------------------------------------------------------------*/
/* Creating new views */
/*-------------------------------------------------------------------------*/

CREATE VIEW TOVAR_VW(
    ID,
    NAME,
    EDIZ)
AS
select
  t.oid,t.name,t.ediz
from
  tovar t
where
  delmarked=0 and
  t.tara=2
;
